    case woody = "Woody"
    case oriental = "Oriental"
    case fresh = "Fresh"
    case citrus = "Citrus"
    case aquatic = "Aquatic"
    case gourmand = "Gourmand"
    case spicy = "Spicy"
    case herbal = "Herbal"
}
